bool map_is_wall(int x, int y) {
    // Return true for out-of-bounds coordinates to prevent illegal memory reads.
    if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT) {
        return true;
    }

    // Evaluates to true if the structural tile code at the position is non-zero.
    return world_map[x][y] > 0;
}